/**
 * Computes the hyperbolic tangent of a given number.
 * 
 * @param x - the number to compute the hyperbolic tangent of.
 * @returns the hyperbolic tangent of the given number.
 * @example
 * tanh(0.5) // 0.46211715726000944
 */
export default function tanh( x: number ): number
{
	return Math.tanh( x );
}
